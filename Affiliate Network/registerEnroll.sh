#!/bin/bash

function createAllStar(){
    echo 'Enrolling the CA of Parent club'
    mkdir -p organizations/peerOrganizations/allStar.club.com/
    export FABRIC_CA_CLIENT_HOME=${PWD}/organizations/peerOrganizations/allStar.club.com/
    set -x
    fabric-ca-client enroll -u https://admin:adminpw@localhost:7054 --caname ca-allStar --tls.certfiles "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem"
    { set +x; } 2>/dev/null
      echo 'NodeOUs:
  Enable: true
  ClientOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-allStar.pem
    OrganizationalUnitIdentifier: client
  PeerOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-allStar.pem
    OrganizationalUnitIdentifier: peer
  AdminOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-allStar.pem
    OrganizationalUnitIdentifier: admin
  OrdererOUIdentifier:
    Certificate: cacerts/localhost-7054-ca-allStar.pem
    OrganizationalUnitIdentifier: orderer' > "${PWD}/organizations/peerOrganizations/allStar.club.com/msp/config.yaml"
     # Since the CA serves as both the organization CA and TLS CA, copy the org's root cert that was generated by CA startup into the org level ca and tlsca directories

  # Copy allStar's CA cert to allStar's /msp/tlscacerts directory (for use in the channel MSP definition)
  mkdir -p "${PWD}/organizations/peerOrganizations/allStar.club.com/msp/tlscacerts"
  cp "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem" "${PWD}/organizations/peerOrganizations/allStar.club.com/msp/tlscacerts/ca.crt"

  # Copy allStar's CA cert to allStar's /tlsca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/allStar.club.com/tlsca"
  cp "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem" "${PWD}/organizations/peerOrganizations/allStar.club.com/tlsca/tlsca.allStar.club.com-cert.pem"

  # Copy allStar's CA cert to allStar's /ca directory (for use by clients)
  mkdir -p "${PWD}/organizations/peerOrganizations/allStar.club.com/ca"
  cp "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem" "${PWD}/organizations/peerOrganizations/allStar.club.com/ca/ca.allStar.club.com-cert.pem"

  # Registering peer0
echo "Registering peer0"
set -x
fabric-ca-client register --caname ca-allStar --id.name peer0 --id.secret peer0pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem"
{ set +x; } 2>/dev/null

# Registering peer1
echo "Registering peer1"
set -x
fabric-ca-client register --caname ca-allStar --id.name peer1 --id.secret peer1pw --id.type peer --tls.certfiles "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem"
{ set +x; } 2>/dev/null

# Registering user
echo "Registering user"
set -x
fabric-ca-client register --caname ca-allStar --id.name user1 --id.secret user1pw --id.type client --tls.certfiles "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem"
{ set +x; } 2>/dev/null

# Registering the org admin
echo "Registering the org admin"
set -x
fabric-ca-client register --caname ca-allStar --id.name allStaradmin --id.secret allStaradminpw --id.type admin --tls.certfiles "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem"
{ set +x; } 2>/dev/null

# Generating the peer0 msp
echo "Generating the peer0 msp"
set -x
fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-allStar -M "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer0.allStar.club.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem"
{ set +x; } 2>/dev/null

cp "${PWD}/organizations/peerOrganizations/allStar.club.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer0.allStar.club.com/msp/config.yaml"

# Generating the peer0-tls certificates
echo "Generating the peer0-tls certificates, use --csr.hosts to specify Subject Alternative Names"
set -x
fabric-ca-client enroll -u https://peer0:peer0pw@localhost:7054 --caname ca-allStar -M "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer0.allStar.club.com/tls" --enrollment.profile tls --csr.hosts peer0.allStar.club.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem"
{ set +x; } 2>/dev/null

# Now for peer1
# Generating the peer1 msp
echo "Generating the peer1 msp"
set -x
fabric-ca-client enroll -u https://peer1:peer1pw@localhost:7054 --caname ca-allStar -M "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer1.allStar.club.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem"
{ set +x; } 2>/dev/null

cp "${PWD}/organizations/peerOrganizations/allStar.club.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer1.allStar.club.com/msp/config.yaml"

# Generating the peer1-tls certificates
echo "Generating the peer1-tls certificates, use --csr.hosts to specify Subject Alternative Names"
set -x
fabric-ca-client enroll -u https://peer1:peer1pw@localhost:7054 --caname ca-allStar -M "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer1.allStar.club.com/tls" --enrollment.profile tls --csr.hosts peer1.allStar.club.com --csr.hosts localhost --tls.certfiles "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem"
{ set +x; } 2>/dev/null

 # Copy the tls CA cert, server cert, server keystore to well-known file names in peer0's tls directory
cp "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer0.allStar.club.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer0.allStar.club.com/tls/ca.crt"
cp "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer0.allStar.club.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer0.allStar.club.com/tls/server.crt"
cp "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer0.allStar.club.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer0.allStar.club.com/tls/server.key"

# For peer1
# Copy the tls CA cert, server cert, server keystore to well-known file names in peer1's tls directory
cp "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer1.allStar.club.com/tls/tlscacerts/"* "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer1.allStar.club.com/tls/ca.crt"
cp "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer1.allStar.club.com/tls/signcerts/"* "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer1.allStar.club.com/tls/server.crt"
cp "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer1.allStar.club.com/tls/keystore/"* "${PWD}/organizations/peerOrganizations/allStar.club.com/peers/peer1.allStar.club.com/tls/server.key"

# Generating the user msp
echo "Generating the user msp"
set -x
fabric-ca-client enroll -u https://user1:user1pw@localhost:7054 --caname ca-allStar -M "${PWD}/organizations/peerOrganizations/allStar.club.com/users/User1@allStar.club.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem"
{ set +x; } 2>/dev/null

cp "${PWD}/organizations/peerOrganizations/allStar.club.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/allStar.club.com/users/User1@allStar.club.com/msp/config.yaml"

# Generating the org admin msp
echo "Generating the org admin msp"
set -x
fabric-ca-client enroll -u https://allStaradmin:allStaradminpw@localhost:7054 --caname ca-allStar -M "${PWD}/organizations/peerOrganizations/allStar.club.com/users/Admin@allStar.club.com/msp" --tls.certfiles "${PWD}/organizations/fabric-ca/allStar/ca-cert.pem"
{ set +x; } 2>/dev/null

cp "${PWD}/organizations/peerOrganizations/allStar.club.com/msp/config.yaml" "${PWD}/organizations/peerOrganizations/allStar.club.com/users/Admin@allStar.club.com/msp/config.yaml"

}

